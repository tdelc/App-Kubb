# ------------------------------------------------------------------
# db_post.R — Couche d'accès PostgreSQL (remplace db.R)
# ------------------------------------------------------------------
# Mêmes fonctions exportées que db.R (db_connect, db_init, db_touch,
# db_version, get_*, add_transaction), plus deux wrappers dbx_exec /
# dbx_get qui traduisent les placeholders '?' (style SQLite) en
# '$1, $2, ...' (style RPostgres). Les modules peuvent donc garder
# leurs requêtes telles quelles, à trois exceptions près (voir le
# message d'accompagnement).
#
# Connexion : lit DATABASE_URL (fournie par Railway), sinon les
# variables PGHOST / PGPORT / PGDATABASE / PGUSER / PGPASSWORD
# (pratiques pour le développement local).
#
# Dépendances : RPostgres, pool (à la place de RSQLite).
# ------------------------------------------------------------------

